package switchcase_class;

public class Switch_Case {

	public static void main(String[] args) {
		switch(5)
		{
		case 1:
			System.out.println("Your logic will be here");
			break;
		case 2:
			System.out.println("Your second logic will be here");
			break;
			
		case 3:
			System.out.println("Your third logic will be here");
			break;
		case 4:
			System.out.println("Your fourth logic will be here");
			break;
			default:
				System.out.println("The selection you have made is wrong");
			
		}

	}

}
